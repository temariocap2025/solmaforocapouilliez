        <h2 class="panel-title">¿Qué es el solmáforo Capouilliez?</h2>
    </div>
    
    <p style="font-size: 1.1rem; line-height: 1.6; color: #374151; margin-bottom: 2rem;">
        Un solmáforo es un dispositivo visual que indica el nivel de radiación ultravioleta (UV) que hay en el ambiente. 
        Funciona como un semáforo, usando colores (generalmente del verde al morado) para mostrar qué tan fuerte está 
        la radiación solar en un momento determinado. Así, las personas pueden saber si es seguro estar al aire libre 
        o si es mejor protegerse del sol.
    </p>

